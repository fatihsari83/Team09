package day_03_01_ForLoop;

public class P01 {
    public static void main(String[] args) {
        /*
        Soru 1- 1’den 100’e kadar sayilari ayni satirda aralarinda bir bosluk birakarak
                yazdirin.
         */
        for (int i = 1; i <=100 ; i++) {
            System.out.print(i+" ");

        }
    }
}
